uint8_t DHT11_Check_Response(void)
{
    uint8_t Response = 0;

    delay_us(40);

    if(!(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1)))
    {
        delay_us(80);

        if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1))
        {
            Response = 1;
        }

        while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1));
    }

    return Response;
}
uint8_t DHT11_Read(void)
{
    uint8_t i=0, j;

    for(j = 0; j < 8; j++)
    {
        while(!(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1)));

        delay_us(40);

        if(!(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1)))
        {
            i &= ~(1 << (7 - j));
        }
        else
        {
            i |= (1 << (7 - j));

            while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1));
        }
    }

    return i;
}
